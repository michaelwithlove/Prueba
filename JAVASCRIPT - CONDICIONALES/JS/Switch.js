// multiples condiciones en base a un dato
const nombre = "Juan";

switch (nombre) {
  case "Juan":
    console.log("Bienvenido Juan");
    break;
  case "Matias":
    console.log("Bienvenido Matias");
    break;
  case "Fran":
    console.log("Bienvenido Fran");
    break;
  case "Luis":
    console.log("Bienvenido Luis");
    break;
  default:
    console.log("No tenemos ningun saludo para vos");
}